
public class GarageDoor {
//metodos para abrir, fechar e parar a garagem
    public void up() {
        System.out.println("A garagem esta ABERTA");
    }

    public void down() {
        System.out.println("A garagem esta FECHADA");
    }

    public void stop() {
        System.out.println("A garagem esta PARADA");
    }
}